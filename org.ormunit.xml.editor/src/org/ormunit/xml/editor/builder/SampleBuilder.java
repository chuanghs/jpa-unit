public class SampleBuilder extends IncrementalProjectBuilder {

	class SampleDeltaVisitor implements IResourceDeltaVisitor {
		/*
		 * (non-Javadoc)
		 * 
		 * @see
		 * org.eclipse.core.resources.IResourceDeltaVisitor#visit(org.eclipse
		 * .core.resources.IResourceDelta)
		 */
		public boolean visit(IResourceDelta delta) throws CoreException {
			IResource resource = delta.getResource();
			Object adapter = resource.getAdapter(IJavaElement.class);
			switch (delta.getKind()) {
			case IResourceDelta.ADDED:
				// handle added resource
				checkXML(resource);
				break;
			case IResourceDelta.REMOVED:
				// handle removed resource
				break;
			case IResourceDelta.CHANGED:
				// handle changed resource
				checkXML(resource);
				break;
			}
			// return true to continue visiting children.
			return true;
		}
	}

	class SampleResourceVisitor implements IResourceVisitor {
		public boolean visit(IResource resource) {
			checkXML(resource);
			// return true to continue visiting children.
			return true;
		}
	}

	class XMLErrorHandler extends DefaultHandler {

		private IFile file;

		public XMLErrorHandler(IFile file) {
			this.file = file;
		}

		private void addMarker(SAXParseException e, int severity) {
			SampleBuilder.this.addMarker(file, e.getMessage(),
					e.getLineNumber(), severity);
		}

		public void error(SAXParseException exception) throws SAXException {
			addMarker(exception, IMarker.SEVERITY_ERROR);
		}

		public void fatalError(SAXParseException exception) throws SAXException {
			addMarker(exception, IMarker.SEVERITY_ERROR);
		}

		public void warning(SAXParseException exception) throws SAXException {
			addMarker(exception, IMarker.SEVERITY_WARNING);
		}
	}

	public static final String BUILDER_ID = "fdgsdfgsdfg.sampleBuilder";

	private static final String MARKER_TYPE = "fdgsdfgsdfg.xmlProblem";

	private SAXParserFactory parserFactory;

	private void addMarker(IFile file, String message, int lineNumber,
			int severity) {
		try {
			IMarker marker = file.createMarker(MARKER_TYPE);
			marker.setAttribute(IMarker.MESSAGE, message);
			marker.setAttribute(IMarker.SEVERITY, severity);
			if (lineNumber == -1) {
				lineNumber = 1;
			}
			marker.setAttribute(IMarker.LINE_NUMBER, lineNumber);
		} catch (CoreException e) {
		}
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.core.internal.events.InternalBuilder#build(int,
	 * java.util.Map, org.eclipse.core.runtime.IProgressMonitor)
	 */
	protected IProject[] build(int kind, Map args, IProgressMonitor monitor)
			throws CoreException {
		if (kind == FULL_BUILD) {
			fullBuild(monitor);
		} else {
			IResourceDelta delta = getDelta(getProject());
			if (delta == null) {
				fullBuild(monitor);
			} else {
				incrementalBuild(delta, monitor);
			}
		}
		return null;
	}

	void checkXML(IResource resource) {
		if (resource instanceof IFile) {
			IJavaElement adapter = (IJavaElement) resource
					.getAdapter(IJavaElement.class);
			if (adapter != null && adapter instanceof IParent) {
				long a = System.nanoTime();
				try {
					processChildren((IParent) adapter, "");
				} catch (JavaModelException e) {
				}
				System.out.println((System.nanoTime() - 1) / 1000000.0 + "ms");
			}

			if (resource.getName().endsWith(".xml")) {
				IFile file = (IFile) resource;
				deleteMarkers(file);
				XMLErrorHandler reporter = new XMLErrorHandler(file);
				try {
					getParser().parse(file.getContents(), reporter);
				} catch (Exception e1) {
				}
			}
		}
	}

	private void processChildren(IParent javaProject, String intendation)
			throws JavaModelException {
		
		
		IJavaElement[] children = javaProject.getChildren();
		for (IJavaElement je : children) {
			if (je instanceof IImportDeclaration){
				IImportDeclaration id = ((IImportDeclaration)je);
				System.out.println(intendation+id.getElementName());
			}
			if (je instanceof IMethod){
				IMethod m = (IMethod) je;
				System.out.println(intendation
						+m.getElementName()
						+", "+m.getReturnType());
			}
			// System.out.println(intendation+je.getClass().getCanonicalName());
			if (je instanceof IParent) {
				processChildren((IParent) je, intendation + " ");
			}
			if (je instanceof IType) {
				IMethod[] methods = ((IType) je).getMethods();
				
				IField[] fields = ((IType) je).getFields();
				for (IField f : fields) {
					System.out.println(intendation +
							
							f.getFlags()
							+", "+Signature.getElementType(f.getTypeSignature())
							+", "+f.getElementName());
				}
			}
		}

	}

	private void deleteMarkers(IFile file) {
		try {
			file.deleteMarkers(MARKER_TYPE, false, IResource.DEPTH_ZERO);
		} catch (CoreException ce) {
		}
	}

	protected void fullBuild(final IProgressMonitor monitor)
			throws CoreException {
		try {
			getProject().accept(new SampleResourceVisitor());
		} catch (CoreException e) {
		}
	}

	private SAXParser getParser() throws ParserConfigurationException,
			SAXException {
		if (parserFactory == null) {
			parserFactory = SAXParserFactory.newInstance();
		}
		return parserFactory.newSAXParser();
	}

	protected void incrementalBuild(IResourceDelta delta,
			IProgressMonitor monitor) throws CoreException {
		// the visitor does the work.
		delta.accept(new SampleDeltaVisitor());
	}
}
